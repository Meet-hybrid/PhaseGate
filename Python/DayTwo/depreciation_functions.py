def get_depreciated_value(percentage):
	if percentage == 8:
		return percentage / 100 * 50000
	elif percentage == 8 * 2:
		return percentage / 100 * 46000
	elif percentage == 8 * 3:
		return percentage / 100 * 38640
	elif percentage == 8 * 4:
		return percentage / 100 * 29366.0
	elif percentage == 8 * 5:
		return percentage / 100 * 19968.0